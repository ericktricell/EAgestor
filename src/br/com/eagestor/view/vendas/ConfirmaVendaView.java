    private void salvarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTActionPerformed
        Recebimento r = new Recebimento();
        r.setIdCaixa(new FinanceiroDAO().getCaixaDia());
        r.setFormaPagamento(pagamentoCB.getSelectedItem().toString());
        r.setDataRecebimento(new Date());
        r.setTotal(total);
        if(avistaRA.isSelected()){
            r.setNumParcelas(Integer.parseInt(vezesField.getText()));
            r.setVlrParcelas(vlrParcela);
        }
        r.setVendaprodutoList(listvenda);
        if (avistaRA.isSelected())
            r.setTipoPagamento(avistaRA.getText());
        else if (parceladoRA.isSelected())
            r.setTipoPagamento(parceladoRA.getText());
        boolean ca = true;
        if (!(listCli.isEmpty())){
            r.setIdCliente(listCli.get(clienteCB.getSelectedIndex() - 1));
            ca = true;
        } else if(clienteCB.getSelectedIndex() == 0 || listCli.isEmpty()){
            ca = false;
        }
        
        String cod = String.valueOf(new FinanceiroDAO().getIdRecebimento());
        
        new FinanceiroBean().registroVenda(r, ca, cod, totalRecField.getText(), trocoField.getText());
        this.dispose();
    }//GEN-LAST:event_salvarBTActionPerformed


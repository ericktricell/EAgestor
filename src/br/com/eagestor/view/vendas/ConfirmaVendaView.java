    private void carregaClientes(){
        listCli = new ClienteDAO().busca();
        
        clienteCB.removeAllItems();
        clienteCB.addItem("Venda Avulsa");
        for (Cliente cli : listCli) {
            clienteCB.addItem(cli.getNome() + " " + cli.getSobrenome());
        }
    }
    
    private void tipoVenda(){
        if (avistaRA.isSelected()){
            vezesField.setText("1");
            valorPField.setText(totalVenda.getText());
            vlrParcela = total;
            vezesField.setEditable(false);
            valorPField.setEditable(false);
            totalRecField.requestFocus();
        } else if (parceladoRA.isSelected()){
            vezesField.setEditable(true);
            valorPField.setEditable(true);
            vezesField.requestFocus();
        }
    }
    

    private void totalRecFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_totalRecFieldKeyReleased
        if (!(totalRecField.getText().equals(""))){
            double vlrRecebido = Double.parseDouble(totalRecField.getText());
            double troco = vlrRecebido - total;
            trocoField.setText(new DecimalFormat("¤#,##0.00").format(troco));
        }
    }//GEN-LAST:event_totalRecFieldKeyReleased

    private void vezesFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_vezesFieldKeyReleased
        if (!(vezesField.getText().equals(""))){
            int v = Integer.parseInt(vezesField.getText());
            vlrParcela = total / v;
            valorPField.setText(new DecimalFormat("¤#,##0.00").format(vlrParcela));
        }
    }//GEN-LAST:event_vezesFieldKeyReleased


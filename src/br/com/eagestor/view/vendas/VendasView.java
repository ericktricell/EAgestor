    private void organizaTabela(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Produto");        
        model.addColumn("Quantidade");        
        model.addColumn("Medida");        
        model.addColumn("Valor Unitario");        
        model.addColumn("Total");        
        
        for (Vendaproduto vp1 : listvenda){
            model.addRow(new String[]{
                vp1.getProdutos().getNome(),
                String.valueOf(vp1.getQuantidade()),
                vp1.getProdutos().getIdMedida().getDescricao(),
                new DecimalFormat("¤#,##0.00").format(vp1.getProdutos().getValorVenda()),
                new DecimalFormat("¤#,##0.00").format(vp1.getQuantidade() * vp1.getProdutos().getValorVenda())
            });
        }
        
        jTable1.setModel(model);
    }
    
    private void carregatotal(){
        double total = 0;
        for (Vendaproduto vp1 : listvenda){
            total += vp1.getQuantidade() * vp1.getProdutos().getValorVenda();
        }
        
        valorTotal.setText(new DecimalFormat("¤#,##0.00").format(total));
    }
    
    public void setAcessibilidade() {
        JRootPane meurootpane = getRootPane();
        meurootpane.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "ESCAPE");
        meurootpane.getRootPane().getActionMap().put("ESCAPE", new AbstractAction("ESCAPE") {
            @Override
            public void actionPerformed(ActionEvent e) {
                int i = JOptionPane.showConfirmDialog(null, "Deseja realmente sair?");
                if (i == 0){
                    dispose();
                }
                
            }
        });    
    }

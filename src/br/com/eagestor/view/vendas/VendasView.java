    private void codigoFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoFieldKeyReleased
        if(evt.getKeyCode() == KeyEvent.VK_F3){
            BuscaProdutos bp = new BuscaProdutos(new javax.swing.JFrame(), true);
            bp.setVisible(true);
            try{
                prod = bp.getProd();
                produtoField.setText(prod.getNome());
                vlrUnit.setText(new DecimalFormat("¤#,##0.00").format(prod.getValorVenda()));
                codigoField.setText(prod.getCodigo());

                vp =  new Vendaproduto();
                vppk = new VendaprodutoPK();
                vppk.setIdProduto(prod.getIdProduto());
                vppk.setIdRecebimento(new FinanceiroDAO().getIdRecebimento());
                vp.setVendaprodutoPK(vppk);
                vp.setProdutos(prod);
                vp.setValorVenda(prod.getValorVenda());

                listvenda.add(vp);
                this.organizaTabela();

                qntField.requestFocus();
            } catch(NullPointerException e){
                
            }
        }
        
    }//GEN-LAST:event_codigoFieldKeyReleased

    private void qntFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_qntFieldKeyReleased
        try{
            if (!(qntField.getText().equals(""))){
                if (Double.parseDouble(qntField.getText().replace(",", ".")) <= prod.getQuantidade()){
                    listvenda.get(listvenda.size() - 1).setQuantidade(Double.parseDouble(qntField.getText()));
                    double baixa = prod.getQuantidade() - Double.parseDouble(qntField.getText().replace(",", "."));
                    prod.setQuantidade(baixa);
                    listvenda.get(listvenda.size() - 1).setProdutos(prod);
                    this.organizaTabela();
                    this.carregatotal();
                } else{
                    JOptionPane.showMessageDialog(null, "Quantidade Indisponivel");
                    qntField.setText("");
                }
            }
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Digite apenas numeros");
            qntField.setText("");
        }
    }//GEN-LAST:event_qntFieldKeyReleased

